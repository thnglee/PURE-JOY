Map::Map() {
	dirt = TextureManager::LoadTexture("assets/Tilesets/ground tiles/old tiles/Tilled Dirt.png");
	grass = TextureManager::LoadTexture("assets/Tilesets/ground tiles/old tiles/Grass.png");
	water = TextureManager::LoadTexture("assets/Tilesets/ground tiles/old tiles/Water.png");

	LoadMap(farm);

	src.x = src.y = 0;
	src.w = dest.w = 0;
	src.h = dest.h = 0;

	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[25][25]) {
	for (int row = 0; row < 25; row++) {
		for (int column = 0; column < 25; column++) {
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap() {
	int type = 0;

	for (int row = 0; row < 25; row++) {
		for (int column = 0; column < 25; column++) {
			type = map[row][column];

			dest.x = column * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(water, src, dest);
				break;
			case 1: 
				TextureManager::Draw(grass, src, dest);
				break;
			case 2:
				TextureManager::Draw(dirt, src, dest);
				break;

			default:
				break;
			}
		}
	}
}